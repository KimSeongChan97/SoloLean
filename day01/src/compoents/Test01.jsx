import React from 'react';

// Test01이라는 이름의 함수형 컴포넌트를 정의하고 있습니다. 함수형 컴포넌트는 JSX를 반환하여 화면에 표시할 내용을 정의합니다.
const Test01 = () => {
    return (
        <div>
            {/* JSX 영역: JSX는 JavaScript 문법과 비슷하지만 HTML 같은 태그를 사용해 UI를 정의할 수 있는 문법입니다. 
                JSX 안에서는 모든 태그를 열면 반드시 닫아야 하며, 닫지 않으면 오류가 발생합니다. */}
            <h3>JSX 영역</h3> 
            {/* <h3> 태그는 HTML에서 세 번째 수준의 제목을 나타내며, 크기가 <h1>, <h2>보다 작습니다. 여기서는 "JSX 영역"이라는 제목을 표시합니다. */}

            <p>태그를 열면 반드시 닫아야 한다.</p> 
            {/* <p> 태그는 단락(paragraph)을 정의합니다. 여기서는 "태그를 열면 반드시 닫아야 한다."는 설명을 단락으로 표시하고 있습니다. 
                JSX의 특징 중 하나는 모든 태그가 스스로 닫히거나, 명시적으로 닫혀야 한다는 것입니다. */}
        </div>
    );
};

export default Test01;
