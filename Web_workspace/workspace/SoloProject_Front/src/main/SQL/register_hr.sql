CREATE TABLE MEMBER (
    MEMBER_ID VARCHAR2(50) PRIMARY KEY,
    NAME VARCHAR2(100) NOT NULL,
    EMAIL VARCHAR2(100) NOT NULL UNIQUE,
    PASSWORD VARCHAR2(100) NOT NULL,
    REGISTER_DATE DATE DEFAULT SYSDATE
);

CREATE TABLE BOARD (
    SEQ NUMBER PRIMARY KEY,
    MEMBER_ID VARCHAR2(50) REFERENCES MEMBER(MEMBER_ID),
    SUBJECT VARCHAR2(200) NOT NULL,
    CONTENT CLOB NOT NULL,
    LOGTIME DATE DEFAULT SYSDATE
);

CREATE SEQUENCE BOARD_SEQ
START WITH 1
INCREMENT BY 1;


CREATE TABLE members (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY,
    name VARCHAR2(100),
    email VARCHAR2(100) UNIQUE,
    password VARCHAR2(100),
    PRIMARY KEY (id)
);

CREATE TABLE posts (
    post_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, -- 게시글 ID (자동 증가)
    title VARCHAR2(255) NOT NULL, -- 제목
    content CLOB NOT NULL, -- 내용 (큰 텍스트 저장을 위해 CLOB 사용)
    author VARCHAR2(100) NOT NULL, -- 작성자
    created_at DATE DEFAULT SYSDATE, -- 작성일 (기본값: 현재 날짜)
    views NUMBER DEFAULT 0 -- 조회수 (기본값: 0)
);

