import React from 'react';

const Test02 = () => {
     // 함수 영역 ( 자바스크립트 영역 ) - 조건문, 제어문 등 가능
     // 이 부분은 JavaScript 로직을 처리하는 영역입니다. 조건문, 반복문 등 모든 JavaScript 문법을 사용할 수 있습니다.
     // 이 영역에서는 리액트 컴포넌트 내부에서 사용할 변수를 선언하거나 로직을 작성할 수 있습니다.
     let title = '신상명세서'; // title 변수는 변경 가능한 값을 저장하며, '신상명세서'라는 문자열을 가지고 있습니다.
     // let으로 선언된 변수는 값이 변경될 수 있으며, 이는 동적으로 변하는 값을 처리할 때 주로 사용됩니다.
     const name = '홍길동'; // name 변수는 상수로 선언되었고, '홍길동'이라는 값을 가지고 있습니다.
     // const로 선언된 변수는 상수로 값을 변경할 수 없습니다. 즉, name 변수는 컴포넌트가 재렌더링 되더라도 값이 변하지 않습니다.
     const age = 25; // age 변수는 상수로, '25'라는 숫자를 가지고 있으며 이 숫자는 나이를 나타냅니다.
     // age도 상수로 설정되어 있어, 이후에 값이 변경되지 않음을 보장합니다. 나이 값이 고정되어 있을 경우 사용하기 적합합니다.
     const addr = '서울'; // addr 변수는 상수로, '서울'이라는 문자열을 가지고 있습니다. 이는 주소를 의미합니다.
     // addr도 마찬가지로 const로 선언된 상수입니다. 이 변수는 주소를 나타내며, 변경되지 않을 값에 적합합니다.

     const css1 = {
          color: 'red',
          backgroundColor: 'yellow',
          fontSize: '30pt',
          padding: 20,
          margin: 10,
          border: '3px solid #000',
          textAlign: 'center'
     };
     // css1은 객체로 정의된 인라인 스타일입니다. 각 속성은 CSS에서 사용되는 스타일 속성입니다.
     // 예를 들어, color는 글자색을 의미하며, backgroundColor는 배경색을 나타냅니다.
     // 이 스타일 객체는 특정 JSX 요소에 적용되어 해당 스타일을 반영합니다.
     // padding, margin과 같은 값들은 각각 요소 내부와 외부의 간격을 설정하는 속성입니다.

     const css2 = {
          width: 400,
          color: '#fff',
          backgroundColor: 'hotpink',
          fontSize: 20,
          padding: 15,
          margin: 30,
          textAlign: 'center'
     };
     // css2 역시 인라인 스타일 객체입니다. 이 객체는 다른 스타일을 적용하며,
     // 각 속성은 camelCase로 작성되어 있습니다. 예를 들어, background-color는 backgroundColor로 변환되어 사용됩니다.
     // textAlign 속성은 글자를 중앙에 정렬하는 역할을 합니다.

     return (
          <>
               {/* JSX 영역 - HTML과 유사한 구조를 사용하여 UI를 정의합니다. */}
               {/* JSX는 자바스크립트에서 HTML과 유사한 구문을 사용해 UI를 선언적으로 작성할 수 있도록 도와줍니다. */}
               <h2>JSX 영역</h2>
               {/* <h2> 태그는 HTML에서 제목을 나타내는 태그로, 두 번째로 큰 크기의 제목을 표시합니다. 'JSX 영역'이라는 텍스트가 표시됩니다. */}
               {/* JSX에서 태그를 사용할 때, 자바스크립트 표현식은 중괄호 { } 안에 넣을 수 있습니다. */}
               <h3 style={css1}>{title}</h3>
               {/* <h3> 태그는 <h2>보다 작은 크기의 제목을 나타냅니다. title 변수에 할당된 '신상명세서' 값이 중괄호 {}를 통해 출력됩니다. */}
               {/* 이 요소는 css1 스타일 객체를 적용받아 빨간 글자와 노란 배경, 중앙 정렬 등의 스타일이 적용됩니다. */}

               <ul>
                    {/* <ul> 태그는 순서 없는 리스트를 정의합니다. 각 항목은 <li> 태그로 감싸서 표시됩니다. */}
                    {/* 순서 없는 리스트로 항목들을 나열할 때 <ul>을 사용하며, 각 항목은 <li>로 구성됩니다. */}
                    <li style={css2}>이름 : {name} </li>
                    {/* <li> 태그는 리스트의 항목을 정의하는데, 여기서는 이름과 name 변수의 값인 '홍길동'이 출력됩니다. */}
                    {/* css2 스타일 객체를 적용하여 글자 색상은 흰색, 배경은 핫핑크, 중앙 정렬 등의 스타일을 적용합니다. */}

                    {/* <li>나이 : {age} 세 { 조건 ? 참 : 거짓 }</li> */}
                    {/* 삼항 연산자를 사용해 조건을 만족하면 '성인', 그렇지 않으면 '청소년'을 출력합니다. */}
                    {/* 삼항 연산자는 age 값에 따라 조건에 맞는 값을 출력합니다. 여기서는 age >= 19가 참이므로 '성인'이 출력됩니다. */}
                    <li style={ {backgroundColor: 'greenyellow', padding: 15, margin: 'auto'   }}>
                         나이 : {age} 세 { age >= 19 ? '성인' : '청소년' }
                    </li>
                    {/* age 값이 19 이상이면 '성인', 그렇지 않으면 '청소년'이 출력됩니다. 현재 age는 25이므로 '성인'이 출력됩니다. */}
                    {/* 인라인 스타일 속성으로 'backgroundColor', 'padding', 'margin'이 적용되었으며, 'greenyellow' 배경색과 중앙 정렬이 됩니다. */}

                    <li style={{ color: 'red' }}>주소 : {addr} </li>
                    {/* style 속성을 사용하여 인라인 스타일을 적용할 수 있습니다. 여기서는 글자 색깔을 빨간색으로 지정하였습니다. 
                    React에서는 스타일을 객체 형태로 지정하며, CSS 속성명은 camelCase로 표기해야 합니다. 
                    '주소 : 서울'이라는 텍스트가 빨간색으로 표시됩니다. */}
                    {/* 스타일 객체에서 color 속성만 설정하여 빨간색 글씨로 출력됩니다. addr 값은 '서울'이므로 '주소 : 서울'이 출력됩니다. */}
               </ul>
          </>
     );
};

export default Test02;
// 이 컴포넌트를 다른 파일에서 사용할 수 있도록 내보내는 구문입니다. export default를 사용하면 다른 파일에서 import를 통해 이 컴포넌트를 사용할 수 있습니다.
// export default는 해당 컴포넌트를 외부에서 사용할 수 있게 만들어주는 역할을 합니다. Test02 컴포넌트를 다른 파일에서 불러와 사용할 수 있습니다.
